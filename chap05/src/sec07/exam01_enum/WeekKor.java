package sec07.exam01_enum;

/*
 * 열거타입 : enum(enumeration type)
 * 1. 타입 이름으로 *.java 파일 생성
 * 2. 첫 문자는 대문자로 시작(관례)
 * 3. 열거상수 : 모두 대문자(관례)
 * 4. 여러 단어로 연결은 언더스코어(_) (관례)
 */
public enum WeekKor {
	  월요일,   // 0번째
	  화요일,   // 1번째
	  수요일,   // 2번째
	  목요일,   // 3번째
	  금요일,   // 4번째
	  토요일,   // 5번째
	  일요일    // 6번째
}
